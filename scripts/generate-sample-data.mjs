// Generates a realistic-looking COE bidding history as a fallback dataset.
// Output matches the data.gov.sg "COE Bidding Results" record shape so the
// app can treat live and sample data identically.
//
// Run: npm run gen:sample
import { writeFileSync, mkdirSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const outPath = join(__dirname, '..', 'src', 'data', 'sampleData.ts')

// Deterministic PRNG so regenerating produces stable output.
let seed = 42
function rand() {
  seed = (seed * 1103515245 + 12345) & 0x7fffffff
  return seed / 0x7fffffff
}
function noise(scale) {
  return (rand() - 0.5) * 2 * scale
}

const START_YEAR = 2010
const END_YEAR = 2026
const END_MONTH = 6 // data through June 2026

// Anchor yearly base premiums (SGD) per category, loosely tracking real history.
// Interpolated month-to-month with seasonality + noise.
const anchors = {
  'Category A': { 2010: 35000, 2013: 88000, 2016: 50000, 2019: 33000, 2020: 38000, 2023: 95000, 2026: 92000 },
  'Category B': { 2010: 42000, 2013: 96000, 2016: 56000, 2019: 38000, 2020: 44000, 2023: 150000, 2026: 118000 },
  'Category C': { 2010: 38000, 2013: 73000, 2016: 48000, 2019: 30000, 2020: 34000, 2023: 88000, 2026: 78000 },
  'Category D': { 2010: 1500, 2013: 3200, 2016: 5800, 2019: 4200, 2020: 5500, 2023: 11500, 2026: 9800 },
  'Category E': { 2010: 44000, 2013: 97000, 2016: 57000, 2019: 39000, 2020: 45000, 2023: 152000, 2026: 120000 },
}

const baseQuota = {
  'Category A': 1200,
  'Category B': 1000,
  'Category C': 350,
  'Category D': 500,
  'Category E': 320,
}

function interp(map, year, month) {
  const years = Object.keys(map).map(Number).sort((a, b) => a - b)
  const t = year + (month - 1) / 12
  let lo = years[0]
  let hi = years[years.length - 1]
  for (let i = 0; i < years.length - 1; i++) {
    if (t >= years[i] && t <= years[i + 1]) {
      lo = years[i]
      hi = years[i + 1]
      break
    }
  }
  if (t <= years[0]) return map[years[0]]
  if (t >= years[years.length - 1]) return map[years[years.length - 1]]
  const frac = (t - lo) / (hi - lo)
  return map[lo] + (map[hi] - map[lo]) * frac
}

const records = []
for (let year = START_YEAR; year <= END_YEAR; year++) {
  const lastMonth = year === END_YEAR ? END_MONTH : 12
  for (let month = 1; month <= lastMonth; month++) {
    for (const vehicle_class of Object.keys(anchors)) {
      for (const bidding_no of [1, 2]) {
        const base = interp(anchors[vehicle_class], year, month)
        // Seasonality: mild dip around Q1 (post-festive), bump mid-year.
        const seasonal = Math.sin(((month - 3) / 12) * 2 * Math.PI) * base * 0.04
        const roundBias = bidding_no === 2 ? base * 0.015 : 0
        let premium = Math.round((base + seasonal + roundBias + noise(base * 0.06)) / 100) * 100
        premium = Math.max(premium, vehicle_class === 'Category D' ? 500 : 5000)

        const quota = Math.max(
          50,
          Math.round((baseQuota[vehicle_class] + noise(baseQuota[vehicle_class] * 0.25)) / 1) ,
        )
        // Oversubscription scales with how "hot" the market is relative to base.
        const heat = premium / base
        const oversub = 1.05 + Math.max(0, heat - 0.95) * 1.4 + Math.max(0, noise(0.15))
        const bids_received = Math.round(quota * oversub)
        const bids_success = Math.min(quota, Math.round(quota * (0.9 + rand() * 0.1)))

        records.push({
          month: `${year}-${String(month).padStart(2, '0')}`,
          bidding_no,
          vehicle_class,
          quota,
          bids_success,
          bids_received,
          premium,
        })
      }
    }
  }
}

const header = `// AUTO-GENERATED by scripts/generate-sample-data.mjs — do not edit by hand.
// Fallback COE bidding dataset used when the live data.gov.sg API is unreachable.
import type { RawCoeRecord } from '../types'

export const sampleRecords: RawCoeRecord[] = `

mkdirSync(dirname(outPath), { recursive: true })
writeFileSync(outPath, header + JSON.stringify(records, null, 0) + '\n')
console.log(`Wrote ${records.length} records to ${outPath}`)
